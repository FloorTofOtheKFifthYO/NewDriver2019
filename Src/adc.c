uint16_t Get_Adc1_4(){
  HAL_ADC_Start(&hadc1);
  HAL_ADC_PollForConversion(&hadc1, 10);   //等待转换完成     
  if(HAL_IS_BIT_SET(HAL_ADC_GetState(&hadc1), HAL_ADC_STATE_REG_EOC))
    return (uint16_t)HAL_ADC_GetValue(&hadc1);       
  else
    return -1;
}
